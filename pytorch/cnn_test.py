        print(x)
        resConv = F.conv2d(x, self.vker1)
        resConv.register_hook(print)
        print(resConv)
        resMax = F.max_pool2d(resConv, 2)
        resMax.register_hook(print)
        print(resMax)
        resRelu = F.relu(resMax)
        resRelu.register_hook(print)
        print(resRelu)
        resConv2 = F.conv2d(resRelu, self.vker2)
